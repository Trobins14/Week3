// NetWorth = Total Assets - TotalDebts
// Assets
let workstationAsset = 2000;
let oldCarAssest = 3000;
// Debts
let creditCardDebt = 2300;
let carLoanDebt = 19000;
// Asset and Debt values
let assetValue = workstationAsset + oldCarAssest;
let debtValue = creditCardDebt + carLoanDebt;
// net worth
let netWorth = assetValue - debtValue;
// calculate
console.log(`Your networth is ` + netWorth);
